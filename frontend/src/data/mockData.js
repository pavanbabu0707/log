// Utility functions
export const formatNumber = (num) => {
  if (num >= 1000000) return (num / 1000000).toFixed(1) + 'M';
  if (num >= 1000) return (num / 1000).toFixed(1) + 'K';
  return num.toString();
};

export const getAuthorityById = (id) => {
  return authorities.find(a => a.id === id);
};

export const getPostsByAuthority = (authorityId) => {
  return posts.filter(p => p.authorityId === authorityId);
};

export const getAuthorityByHandle = (handle) => {
  return authorities.find(a => a.handle === handle);
};
